#!/bin/sh
FILE="relationshipName.txt"
cat <<EOM >$FILE
EOM

echo "___________________________________________________________________________________" >> $FILE
echo "___________________________________________________________________________________" >> $FILE
for d in */ ; do
	NEW_UUID=$(cat /dev/urandom | tr -dc 'a-zA-Z0-9' | fold -w 32 | head -n 1)
    echo "$d => $NEW_UUID" >> $FILE
    echo "-----------------------------------------------------------------------------------" >> $FILE
    mv "$d" "$NEW_UUID"
done

echo "___________________________________________________________________________________" >> $FILE
echo "___________________________________________________________________________________" >> $FILE
